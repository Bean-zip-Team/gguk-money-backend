    public_id UUID NOT NULL UNIQUE,
    user_id UUID NOT NULL,
    tap_date DATE NOT NULL,
    valid_tap_count INTEGER NOT NULL DEFAULT 0,
    point_earned_amount INTEGER NOT NULL DEFAULT 0,
    box_dropped_count INTEGER NOT NULL DEFAULT 0,
    ad_box_open_count INTEGER NOT NULL DEFAULT 0,
    booster_used_count INTEGER NOT NULL DEFAULT 0,
    point_progress_remainder INTEGER NOT NULL DEFAULT 0,
    daily_point_limit_reached BOOLEAN NOT NULL DEFAULT false,
    last_batch_sequence BIGINT,
    version BIGINT NOT NULL DEFAULT 0,
    created_at TIMESTAMPTZ NOT NULL DEFAULT now(),
    updated_at TIMESTAMPTZ NOT NULL DEFAULT now(),
    CONSTRAINT fk_user_tap_daily_user FOREIGN KEY (user_id) REFERENCES app_user (id) ON DELETE RESTRICT,
    CONSTRAINT ux_user_tap_daily UNIQUE (user_id, tap_date),
    CONSTRAINT ck_user_tap_daily_counts CHECK (
        valid_tap_count >= 0
        AND point_earned_amount >= 0
        AND box_dropped_count >= 0
        AND ad_box_open_count >= 0
        AND booster_used_count >= 0
        AND point_progress_remainder >= 0
    )
