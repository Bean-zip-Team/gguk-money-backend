CREATE TABLE app_user (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    public_id UUID NOT NULL UNIQUE,
    status VARCHAR(20) NOT NULL DEFAULT 'ACTIVE',
    nickname VARCHAR(50),
    nickname_normalized VARCHAR(50),
    last_login_at TIMESTAMPTZ,
    created_at TIMESTAMPTZ NOT NULL,
    updated_at TIMESTAMPTZ NOT NULL,
    CONSTRAINT ck_app_user_status CHECK (status IN ('ACTIVE', 'SUSPENDED', 'WITHDRAWN'))
);

CREATE INDEX ix_app_user_status ON app_user (status);
CREATE UNIQUE INDEX ux_app_user_active_nickname_normalized
    ON app_user (nickname_normalized)
    WHERE nickname_normalized IS NOT NULL AND status = 'ACTIVE';

