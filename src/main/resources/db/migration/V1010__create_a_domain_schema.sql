    public_id UUID NOT NULL UNIQUE,
    user_id UUID NOT NULL,
    grant_date DATE NOT NULL,
    daily_sequence INTEGER NOT NULL,
    multiplier NUMERIC(3,1) NOT NULL DEFAULT 2.0,
