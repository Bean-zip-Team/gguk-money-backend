CREATE TABLE point_ledger (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    public_id UUID NOT NULL UNIQUE,
    point_account_id BIGINT NOT NULL,
    user_id UUID NOT NULL,
    entry_type VARCHAR(20) NOT NULL,
    amount BIGINT NOT NULL,
    reason VARCHAR(50) NOT NULL,
    reference_type VARCHAR(40) NOT NULL,
    reference_id UUID NOT NULL,
    idempotency_key UUID,
    balance_after BIGINT NOT NULL,
    occurred_at TIMESTAMPTZ NOT NULL,
    created_at TIMESTAMPTZ NOT NULL DEFAULT now(),
    updated_at TIMESTAMPTZ NOT NULL DEFAULT now(),
    CONSTRAINT fk_point_ledger_account FOREIGN KEY (point_account_id) REFERENCES point_account (id) ON DELETE RESTRICT,
    CONSTRAINT fk_point_ledger_user FOREIGN KEY (user_id) REFERENCES app_user (id) ON DELETE RESTRICT,
    CONSTRAINT ck_point_ledger_entry_type CHECK (entry_type IN ('CREDIT', 'DEBIT', 'REVERSAL')),
    CONSTRAINT ck_point_ledger_amount CHECK (amount > 0),
    CONSTRAINT ck_point_ledger_balance_after CHECK (balance_after >= 0),
    CONSTRAINT ux_point_ledger_reference UNIQUE (user_id, reference_type, reference_id, reason)
);

CREATE UNIQUE INDEX ux_point_ledger_idempotency_key
    ON point_ledger (idempotency_key)
    WHERE idempotency_key IS NOT NULL;

CREATE TABLE cashout_request (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    public_id UUID NOT NULL UNIQUE,
    user_id UUID NOT NULL,
    point_amount BIGINT NOT NULL,
    toss_point_amount BIGINT NOT NULL,
    exchange_rate NUMERIC(5,2) NOT NULL DEFAULT 0.70,
    status VARCHAR(20) NOT NULL DEFAULT 'PENDING',
    idempotency_key UUID NOT NULL,
    provider_transfer_id VARCHAR(255),
    provider_code VARCHAR(80),
    failure_code VARCHAR(80),
    requested_at TIMESTAMPTZ NOT NULL DEFAULT now(),
    completed_at TIMESTAMPTZ,
    version BIGINT NOT NULL DEFAULT 0,
    created_at TIMESTAMPTZ NOT NULL DEFAULT now(),
    updated_at TIMESTAMPTZ NOT NULL DEFAULT now(),
    CONSTRAINT fk_cashout_request_user FOREIGN KEY (user_id) REFERENCES app_user (id) ON DELETE RESTRICT,
    CONSTRAINT ux_cashout_request_idem UNIQUE (user_id, idempotency_key),
    CONSTRAINT ck_cashout_request_status CHECK (status IN ('PENDING', 'PROCESSING', 'SUCCEEDED', 'FAILED', 'CANCELED')),
    CONSTRAINT ck_cashout_request_amounts CHECK (point_amount > 0 AND toss_point_amount > 0)
);

CREATE UNIQUE INDEX ux_cashout_request_provider_transfer
    ON cashout_request (provider_transfer_id)
    WHERE provider_transfer_id IS NOT NULL;
CREATE INDEX ix_cashout_request_user_time ON cashout_request (user_id, requested_at DESC);
CREATE INDEX ix_cashout_request_status ON cashout_request (status, requested_at);

