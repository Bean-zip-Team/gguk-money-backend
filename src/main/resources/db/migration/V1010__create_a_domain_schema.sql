CREATE TABLE keycap_drop_table (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    public_id UUID NOT NULL UNIQUE,
    name VARCHAR(100) NOT NULL,
    priority INTEGER NOT NULL DEFAULT 0,
    purpose VARCHAR(20) NOT NULL DEFAULT 'STANDARD',
    active_from TIMESTAMPTZ NOT NULL,
    active_until TIMESTAMPTZ,
    active BOOLEAN NOT NULL DEFAULT true,
    created_at TIMESTAMPTZ NOT NULL,
    updated_at TIMESTAMPTZ NOT NULL,
    CONSTRAINT ck_keycap_drop_table_purpose CHECK (purpose IN ('STANDARD')),
    CONSTRAINT ck_keycap_drop_table_period CHECK (active_until IS NULL OR active_until > active_from)
);

CREATE INDEX ix_keycap_drop_table_active ON keycap_drop_table (active, active_from, active_until, priority);

CREATE TABLE keycap_drop_item (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    public_id UUID NOT NULL UNIQUE,
    drop_table_id BIGINT NOT NULL,
    keycap_id BIGINT NOT NULL,
    grant_mode VARCHAR(20) NOT NULL DEFAULT 'SHARD',
    shard_count INTEGER,
    weight INTEGER NOT NULL,
    created_at TIMESTAMPTZ NOT NULL,
    updated_at TIMESTAMPTZ NOT NULL,
    CONSTRAINT fk_keycap_drop_item_drop_table FOREIGN KEY (drop_table_id) REFERENCES keycap_drop_table (id) ON DELETE RESTRICT,
    CONSTRAINT fk_keycap_drop_item_keycap FOREIGN KEY (keycap_id) REFERENCES keycap (id) ON DELETE RESTRICT,
    CONSTRAINT ck_keycap_drop_item_grant_mode CHECK (grant_mode IN ('SHARD', 'COMPLETE_KEYCAP')),
    CONSTRAINT ck_keycap_drop_item_shard_count CHECK (
        (grant_mode = 'SHARD' AND shard_count IS NOT NULL AND shard_count > 0)
        OR (grant_mode = 'COMPLETE_KEYCAP' AND shard_count IS NULL)
    ),
    CONSTRAINT ck_keycap_drop_item_weight CHECK (weight > 0)
);

CREATE INDEX ix_keycap_drop_item_table ON keycap_drop_item (drop_table_id);

