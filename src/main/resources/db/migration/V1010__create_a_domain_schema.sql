CREATE TABLE app_user (
    id UUID PRIMARY KEY,
    status VARCHAR(20) NOT NULL DEFAULT 'ACTIVE',
    nickname VARCHAR(50),
    nickname_normalized VARCHAR(50),
    profile_image_url TEXT,
    onboarding_reward_claimed BOOLEAN NOT NULL DEFAULT false,
    onboarding_completed_at TIMESTAMPTZ,
    last_login_at TIMESTAMPTZ,
    withdrawn_at TIMESTAMPTZ,
    created_at TIMESTAMPTZ NOT NULL DEFAULT now(),
    updated_at TIMESTAMPTZ NOT NULL DEFAULT now(),
    CONSTRAINT ck_app_user_status CHECK (status IN ('ACTIVE', 'SUSPENDED', 'WITHDRAWN')),
    CONSTRAINT ck_app_user_withdrawn_at CHECK ((status = 'WITHDRAWN' AND withdrawn_at IS NOT NULL) OR status <> 'WITHDRAWN')
);

CREATE INDEX ix_app_user_status ON app_user (status);
CREATE UNIQUE INDEX ux_app_user_active_nickname_normalized
    ON app_user (nickname_normalized)
    WHERE nickname_normalized IS NOT NULL AND status = 'ACTIVE';

CREATE TABLE auth_identity (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    public_id UUID NOT NULL UNIQUE,
    user_id UUID NOT NULL,
    provider VARCHAR(30) NOT NULL,
    provider_user_id VARCHAR(255) NOT NULL,
    linked_at TIMESTAMPTZ NOT NULL DEFAULT now(),
    created_at TIMESTAMPTZ NOT NULL DEFAULT now(),
    updated_at TIMESTAMPTZ NOT NULL DEFAULT now(),
    CONSTRAINT fk_auth_identity_user FOREIGN KEY (user_id) REFERENCES app_user (id) ON DELETE RESTRICT,
    CONSTRAINT ck_auth_identity_provider CHECK (provider IN ('TOSS')),
    CONSTRAINT ux_auth_identity_provider_user UNIQUE (provider, provider_user_id),
    CONSTRAINT ux_auth_identity_user_provider UNIQUE (user_id, provider)
);

CREATE INDEX ix_auth_identity_user_id ON auth_identity (user_id);

CREATE TABLE app_config (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    public_id UUID NOT NULL UNIQUE,
    config_key VARCHAR(80) NOT NULL,
    config_value JSONB NOT NULL,
    effective_at TIMESTAMPTZ NOT NULL,
    created_by VARCHAR(80),
    created_at TIMESTAMPTZ NOT NULL DEFAULT now(),
