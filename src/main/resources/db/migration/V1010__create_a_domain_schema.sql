CREATE INDEX ix_app_config_active ON app_config (config_key, effective_at);

CREATE TABLE keycap (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    public_id UUID NOT NULL UNIQUE,
    code VARCHAR(60) NOT NULL UNIQUE,
    name VARCHAR(80) NOT NULL,
    grade VARCHAR(20) NOT NULL,
    required_shard_count INTEGER NOT NULL,
    season INTEGER NOT NULL DEFAULT 1,
    image_url TEXT,
    sound_url TEXT,
    sort_order INTEGER NOT NULL DEFAULT 0,
    active BOOLEAN NOT NULL DEFAULT true,
    created_at TIMESTAMPTZ NOT NULL DEFAULT now(),
    updated_at TIMESTAMPTZ NOT NULL DEFAULT now(),
    CONSTRAINT ck_keycap_grade CHECK (grade IN ('COMMON', 'RARE', 'EPIC', 'LEGENDARY')),
    CONSTRAINT ck_keycap_required_shard_count CHECK (required_shard_count > 0)
);

CREATE INDEX ix_keycap_active_sort ON keycap (active, sort_order);

CREATE TABLE user_keycap (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    public_id UUID NOT NULL UNIQUE,
    user_id UUID NOT NULL,
    keycap_id BIGINT NOT NULL,
    shard_count INTEGER NOT NULL DEFAULT 0,
    status VARCHAR(20) NOT NULL DEFAULT 'IN_PROGRESS',
    equipped BOOLEAN NOT NULL DEFAULT false,
    completed_at TIMESTAMPTZ,
    created_at TIMESTAMPTZ NOT NULL DEFAULT now(),
    updated_at TIMESTAMPTZ NOT NULL DEFAULT now(),
    CONSTRAINT fk_user_keycap_user FOREIGN KEY (user_id) REFERENCES app_user (id) ON DELETE RESTRICT,
    CONSTRAINT fk_user_keycap_keycap FOREIGN KEY (keycap_id) REFERENCES keycap (id) ON DELETE RESTRICT,
    CONSTRAINT ck_user_keycap_shard_count CHECK (shard_count >= 0),
    CONSTRAINT ck_user_keycap_status CHECK (status IN ('IN_PROGRESS', 'COMPLETED')),
    CONSTRAINT ux_user_keycap_user_keycap UNIQUE (user_id, keycap_id)
);

CREATE UNIQUE INDEX ux_user_keycap_equipped ON user_keycap (user_id) WHERE equipped = true;
CREATE INDEX ix_user_keycap_user_status ON user_keycap (user_id, status);

