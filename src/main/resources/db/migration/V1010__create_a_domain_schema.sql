CREATE TABLE tap_batch (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    public_id UUID NOT NULL UNIQUE,
    user_id UUID NOT NULL,
    tap_session_id UUID NOT NULL,
    sequence BIGINT NOT NULL,
    submitted_count INTEGER NOT NULL,
    accepted_count INTEGER NOT NULL DEFAULT 0,
    rejected_count INTEGER NOT NULL DEFAULT 0,
    started_at TIMESTAMPTZ NOT NULL,
    ended_at TIMESTAMPTZ NOT NULL,
    elapsed_ms INTEGER NOT NULL,
    interval_stats JSONB,
    risk_level VARCHAR(20) NOT NULL DEFAULT 'NORMAL',
    status VARCHAR(20) NOT NULL DEFAULT 'RECEIVED',
    request_hash VARCHAR(255) NOT NULL,
    processed_at TIMESTAMPTZ,
    created_at TIMESTAMPTZ NOT NULL DEFAULT now(),
    updated_at TIMESTAMPTZ NOT NULL DEFAULT now(),
    CONSTRAINT fk_tap_batch_user FOREIGN KEY (user_id) REFERENCES app_user (id) ON DELETE RESTRICT,
    CONSTRAINT ux_tap_batch_session_sequence UNIQUE (user_id, tap_session_id, sequence),
    CONSTRAINT ck_tap_batch_risk_level CHECK (risk_level IN ('NORMAL', 'SUSPICIOUS', 'BLOCKED')),
    CONSTRAINT ck_tap_batch_status CHECK (status IN ('RECEIVED', 'ACCEPTED', 'PARTIAL', 'REJECTED')),
    CONSTRAINT ck_tap_batch_counts CHECK (submitted_count >= 0 AND accepted_count >= 0 AND rejected_count >= 0 AND accepted_count + rejected_count <= submitted_count),
    CONSTRAINT ck_tap_batch_period CHECK (ended_at >= started_at)
);

CREATE INDEX ix_tap_batch_user_created ON tap_batch (user_id, created_at DESC);
CREATE INDEX ix_tap_batch_status_created ON tap_batch (status, created_at);

